public class Magazine extends MediaItem{

    private int numberIssue;

    public Magazine(int numberIssue) {
        this.numberIssue = numberIssue;
    }

    @Override
    int id() {
        return id();
    }

    @Override
    String titel() {
        return titel();
    }

    @Override
    String author() {
        return author();
    }

    @Override
    int year() {
        return year();
    }
}
